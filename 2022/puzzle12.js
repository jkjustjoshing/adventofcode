const grid = lines.map((line, y) => {
  return line.split("").map((cell, x) => {
    let char;
    if (cell === "S") {
      char = "a";
    } else if (cell === "E") {
      char = "z";
    } else {
      char = cell;
    }
    height = char.charCodeAt() - "a".charCodeAt();

    const obj = {
      x,
      y,
      height,
      isEnd: cell === "E",
    };
    return obj;
  });
});

const find = (grid, startingCoordinates) => {
  let searchTail = [grid[startingCoordinates.y][startingCoordinates.x]];
  searchTail[0].visited = true;
  searchTail[0].visitedIn = 0;

  // For each loop, find all the not-yet-visited tails that aren't dead ends
  const _size = grid.length * grid[0].length;
  for (let i = 0; i < _size; ++i) {
    searchTail = searchTail.flatMap((tail) => {
      // Look left, right, up, down for candidates
      const candidates = [
        grid[tail.y][tail.x - 1],
        grid[tail.y + 1]?.[tail.x],
        grid[tail.y - 1]?.[tail.x],
        grid[tail.y][tail.x + 1],
      ].filter((a) => a && !a.visited & (a.height <= tail.height + 1));
      candidates.forEach((c) => {
        c.visited = true;
        c.visitedIn = tail.visitedIn + 1;
      });
      return candidates;
    });
    const end = searchTail.find((s) => s.isEnd);
    if (end) {
      const visitedIn = end.visitedIn;

      grid.forEach((r) => {
        r.forEach((c) => {
          delete c.visited;
          delete c.visitedIn;
        });
      });
      return visitedIn;
    }
  }
};

